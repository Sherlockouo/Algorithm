#include <iostream>
#include <cstring>
using namespace std;

#define ll long long
#define MOD 1000000009
#define MAXN 100010

ll Exp(ll a,ll n, ll mod){
	ll ans = 1;
	while(n){
		if(n & 1) 
			ans = ans * a % mod;
		a = a * a % mod;
		n >>= 1;
	}
	return ans;
}

ll Inv(ll a, ll p) {
	return Exp(a, p-2, p);
}

ll InvPre[MAXN];
ll zK[MAXN];

int main() {
	int i, j;
	int t;
	ll N, C;
	int K;
	
	ll sqrt5 = 383008016;
	ll invSqrt5 = Inv(sqrt5, MOD);
	ll inv2 = Inv(2, MOD);
	ll x = (sqrt5 + 1)* inv2 % MOD;
	ll y = ( (-sqrt5 + 1) * inv2 % MOD + MOD ) % MOD;
	ll z = invSqrt5;

	for(int i = 0; i < MAXN; ++i) {
		// 1. 预处理 i 的逆元 
		InvPre[i] = Inv(i, MOD);
		// 2. 预处理 5 的二次剩余的逆元的 i 次
		zK[i] = Exp(invSqrt5, i, MOD);
	}
	
	scanf("%d", &t);
	while(t--) {
		scanf("%I64d %I64d %d", &N, &C, &K);
		ll ans = 0;
		ll Ckj = 1;
		
		// 5. 费马小定理 （欧拉定理） 优化 
		C %= (MOD - 1);
		ll NT = (N + 1) % (MOD - 1);
		ll NN = (N + 1) % MOD;
		
		ll xC = Inv( Exp(x, C, MOD), MOD );
		ll yC = Exp(y, C, MOD);
		
		ll xcNow = Exp(x, C * K % (MOD - 1), MOD);
		ll ycNow = 1, sum = 0;
		
		for(int j = 0; j <= K; ++j) {
			// 非性能点，这里是利用了组合数的递推公式： 
			//  C[k][j] = C[k][j-1] * (K-j+1) / j; 
			if(j) {
				Ckj = Ckj * (K - j + 1) % MOD * InvPre[j] % MOD;
			}
			
			// 非性能点 
			ll qj = xcNow * ycNow % MOD;
			xcNow = xcNow * xC % MOD;
			ycNow = ycNow * yC % MOD;
			
			if(qj == 1) {
				// 公比为 1 的等比数列 
				sum = Ckj * NN % MOD;
			}else {
				// 大性能消耗点 (注意：这里一定要用费马小定理，否则超时，而且一定要用 G++ 提交)
				ll invqj = Inv(qj - 1, MOD);
				// 略有性能消耗 
				ll qjN_1 = Exp(qj, NT, MOD) - 1;
				// 无性能消耗 
				sum = qjN_1 * invqj % MOD * Ckj % MOD;
			} 

			// 6. 优化 (-1)^j 
			if(j & 1) {
				ans -= sum;
			}else {
				ans += sum;
			}
			ans %= MOD;
		}
		ans = (zK[K] * ans) % MOD;
		ans = (ans + MOD) % MOD;
		printf("%d\n", (int)ans);
	
	}
	return 0;
}

